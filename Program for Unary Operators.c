//program for Unary Operators
#include <stdio.h>

int main()
{
    int x = 54, y = 65;  
    //Declare the values x&y

    // Unary Operator
    printf("Unary Operators:\n");

    printf("Increment x: %d\n", ++x);   
    // This will give the increment of the give value in X

    printf("Decrement y: %d\n", --y);   
    // This will give the decrement of the give value in Y

    return 0;
}
